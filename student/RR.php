    <h2>Library Rules</h2>
    <ol>
        <li>Library premises are declared as a Silence Zone. Complete silence and strict discipline should be maintained in the Library and the Reading hall.</li>
        <li>Use of mobile phones, for any purpose, is strictly prohibited in the library premises.</li>
        <li>Every student entering the library premises should be wearing a valid College Identity card. It should be produced as and when demanded by any of the college staff members. Students must scan their ID at the library entry gate.</li>
        <li>Home Issue Card: For Degree & Junior College Students will get 1 home issue card. For SFC & M.Com Students will get 2 home issue cards.</li>
        <li>The Identity Card and Home Issue Card are non-transferable. Only the original holder of the cards can use them for borrowing Library materials.</li>
        <li>The Home Issue Card must be used for borrowing books from the Home Issue Section. Textbooks, fiction books, etc. can be borrowed for home lending on this card.</li>
        <li>Students should strictly return the books on or before the due date stamped on the due date slip stuck on the last page of the book.</li>
        <li>In case of late return of books, the students will be charged a fine at Rs.1/- per day. In case of serious default, the amount of the fine may be increased. While calculating the fine, holidays too will be counted.</li>
        <li>Students should pay the fine immediately and collect their library cards from the library. Fines cannot be kept pending under any circumstances.</li>
        <li>Re-issue of the books will depend on the demand for the books requested.</li>
        <li>Students must handle the Library books, periodicals and other library materials with great care. Any attempt to damage the library materials by defacing them, tearing pages, writing or marking on them will be treated as misconduct and strictly dealt with.</li>
        <li>Students should check the books and periodicals for torn or damaged pages before leaving the counter. Such pages should be immediately brought to the notice of the counter staff. If any book or periodical is found to be damaged or torn, the last student issuing the book or periodical will be held responsible.</li>
        <li>The computer terminals provided to the students for Web OPAC facility to search the library database for books should be used with utmost care and only for the facility provided.</li>
        <li>The Internet facility in the Library has been made available for educational purposes only. Students should refrain from using it for any other purpose.</li>
        <li>If the Original Identity card or Home Issue card is lost, it should be reported to the Librarian in writing. Duplicate cards will be issued on completion of all necessary formalities.</li>
        <li>A Duplicate Home Issue card will be issued on payment of Rs.25/- each. In case of any type of misuse or damage against the lost cards, either in the college premises or outside, the original holder of the card will be held responsible.</li>
        <li>All library communications for the students will be in the form of notices displayed from time to time.</li>
        <li>Students should regularly read the notices displayed on the Library notice boards.</li>
        <li>Students must strictly follow all Library rules and regulations. Any disregard of these rules will be reported to the Principal for appropriate disciplinary action.</li>
    </ol>
